#Comparison Operators
# >, <, >=, <=, ==, != IMPORTANT IN REAL PYTHON PROGRAMS
x = 3 > 2
print(x)
x = 2 > 3
print(x)
print("------")
#Greater than or equal to
x = 3 >= 2
print(x)
x = 3 <= 2
print(x)
print("------")
#Equality operator object is equal to another object
x = 3 == 2
print(x)
print("------")
#is not equal to
x = 3 != 2
print(x)