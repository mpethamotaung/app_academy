# .sort(): to sort list
courses = ["History", "Math", "Physics", "CompSci"] # has length of 4, index 0 to 3 (length of list - 1)
courses.sort() #Sorts list in alphabetical order  
print(courses)

nums = [3, 2, 5, 9, 0, 2, 1,]
nums.sort() # Sorts numbers in ascending order
print(nums)
nums.sort(reverse=True) #sorts numbers in descending order
print(nums)