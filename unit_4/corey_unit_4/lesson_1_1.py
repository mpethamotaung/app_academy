courses = ["History", "Math", "Physics", "CompSci"] # has length of 4, index 0 to 3 (length of list - 1)

#List Slicing
print(courses[0:2]) #prints a range of the list from index 0 to index 2 (not including index 2)
print(courses[:2]) #same as above
print(courses[2:]) #starting from index 2, including index 2 to the end of the list